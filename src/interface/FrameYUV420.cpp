void FrameYUV420::drawText(int32_t x, int32_t y, std::string text, ColorYUV color, bool large)
{
    for(const char c : text)
    {
        if(c < 32 || c > 126)
            continue;

        for(uint8_t i = 0; i < 13; i++)
        {
            uint8_t row = letters[c-32][i];
            for(uint8_t j = 0; j < 8; j++)
            {
                if(row & (1 << j))
                {
                    int32_t targetX = x+8-j;
                    int32_t targetY = y-i;

                    if(large)
                    {
                        drawPoint(targetX*2+0, targetY*2+0, color);
                        drawPoint(targetX*2+1, targetY*2+0, color);
                        drawPoint(targetX*2+0, targetY*2+1, color);
                        drawPoint(targetX*2+1, targetY*2+1, color);
                    }
                    else
                    {
                        drawPointThin(targetX, targetY, color);
                    }
                }
            }
        }

        x += 10;
    }
}