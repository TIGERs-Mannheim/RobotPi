FrameYUV420::FrameYUV420(uint8_t* pYData, uint8_t* pUData, uint8_t* pVData, const FrameMetadata& metadata)
:pYData_(pYData), pUData_(pUData), pVData_(pVData), metadata_(metadata)
{
}

void FrameYUV420::drawPoint(int32_t x, int32_t y, ColorYUV color)
{
    if(x < 0 || x >= (int32_t)metadata_.width)
        return;

    if(y < 0 || y >= (int32_t)metadata_.height)
        return;

    uint32_t yWidth = metadata_.width;
    uint32_t uvWidth = yWidth/2;

    pUData_[y/2*uvWidth + x/2] = color.u_;
    pVData_[y/2*uvWidth + x/2] = color.v_;

    pYData_[(y+0)*yWidth + x + 0] = color.y_;
    pYData_[(y+0)*yWidth + x + 1] = color.y_;
    pYData_[(y+1)*yWidth + x + 0] = color.y_;
    pYData_[(y+1)*yWidth + x + 1] = color.y_;
}

void FrameYUV420::drawPointThin(int32_t x, int32_t y, ColorYUV color)
{
    if(x < 0 || x >= (int32_t)metadata_.width)
        return;

    if(y < 0 || y >= (int32_t)metadata_.height)
        return;

    uint32_t yWidth = metadata_.width;
    uint32_t uvWidth = yWidth/2;

    pUData_[y/2*uvWidth + x/2] = color.u_;
    pVData_[y/2*uvWidth + x/2] = color.v_;

    pYData_[y*yWidth + x] = color.y_;
}

void FrameYUV420::drawLine(float x1, float y1, float x2, float y2, ColorYUV color)
{
    // Bresenham's line algorithm
    // taken from: http://rosettacode.org/wiki/Bitmap/Bresenham%27s_line_algorithm#C.2B.2B

    if(!std::isfinite(x1) || !std::isfinite(y1) || !std::isfinite(x2) || !std::isfinite(y2))
        return;

    const bool steep = (fabsf(y2 - y1) > fabsf(x2 - x1));
    if(steep)
    {
        std::swap(x1, y1);
        std::swap(x2, y2);
    }

    if(x1 > x2)
    {
        std::swap(x1, x2);
        std::swap(y1, y2);
    }

    const float dx = x2 - x1;
    const float dy = fabsf(y2 - y1);

    float error = dx / 2.0f;
    const int32_t ystep = (y1 < y2) ? 1 : -1;
    int32_t y = (int)y1;

    const int32_t maxX = (int)x2;
    const int32_t maxPoints = metadata_.width+metadata_.height;
    int32_t pointsDrawn = 0;

    for(int32_t x = (int32_t)x1; x <= maxX; x++)
    {
        if(steep)
        {
            drawPoint(y, x, color);
        }
        else
        {
            drawPoint(x, y, color);
        }

        pointsDrawn++;
        if(pointsDrawn > maxPoints)
            break;

        error -= dy;
        if(error < 0)
        {
            y += ystep;
            error += dx;
        }
    }
}

void FrameYUV420::drawCircle(float x0, float y0, float radius, ColorYUV color)
{
    // taken from: http://rosettacode.org/wiki/Bitmap/Midpoint_circle_algorithm#C

    float f = 1 - radius;
    float ddF_x = 0;
    float ddF_y = -2 * radius;
    float x = 0;
    float y = radius;

    drawPoint(x0, y0 + radius, color);
    drawPoint(x0, y0 - radius, color);
    drawPoint(x0 + radius, y0, color);
    drawPoint(x0 - radius, y0, color);

    while(x < y)
    {
        if(f >= 0)
        {
            y--;
            ddF_y += 2;
            f += ddF_y;
        }

        x++;
        ddF_x += 2;
        f += ddF_x + 1;

        drawPoint(x0 + x, y0 + y, color);
        drawPoint(x0 - x, y0 + y, color);
        drawPoint(x0 + x, y0 - y, color);
        drawPoint(x0 - x, y0 - y, color);
        drawPoint(x0 + y, y0 + x, color);
        drawPoint(x0 - y, y0 + x, color);
        drawPoint(x0 + y, y0 - x, color);
        drawPoint(x0 - y, y0 - x, color);
    }
}

void FrameYUV420::drawRect(float x0, float y0, float x1, float y1, ColorYUV color)
{
    drawLine(x0, y0, x1, y0, color);
    drawLine(x1, y0, x1, y1, color);
    drawLine(x1, y1, x0, y1, color);
    drawLine(x0, y1, x0, y0, color);
}

void FrameYUV420::drawFilledRect(float x1, float y1, float x2, float y2, ColorYUV color)
{
    if(!std::isfinite(x1) || !std::isfinite(y1) || !std::isfinite(x2) || !std::isfinite(y2))
        return;
    
    if(x1 > x2)
    {
        std::swap(x1, x2);
    }
    if(y1 > y2)
    {
        std::swap(y1, y2);
    }

    for(int32_t y = (int)y1; y < (int)y2; y++)
    {
        for(int32_t x = (int)x1; x < (int)x2; x++)
        {
            drawPoint(x, y, color);
        }
    }
}

